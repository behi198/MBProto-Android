package com.blackshadowsgroup.mbproto.encryption.network.exceptions;

/**
 * RequestInputArgumentException class
 */
@SuppressWarnings("unused")
public class RequestInputArgumentException extends RuntimeException {


	public RequestInputArgumentException() {
		super();
	}// constructor


	public RequestInputArgumentException(String message) {
		super(message);
	}// constructor


	public RequestInputArgumentException(String message, Throwable cause) {
		super(message, cause);
	}// constructor


	public RequestInputArgumentException(Throwable cause) {
		super(cause);
	}// constructor


}// RequestInputArgumentException class
